// InputHook.h — перехват ввода через подклассинг WndProc окна игры
//
// Когда оверлей открыт:
//   * обрабатываем горячую клавишу (F8) прямо в WndProc
//   * "глотаем" оконные сообщения ввода (мышь/клавиатура), чтобы игра на них
//     не реагировала, пока пользователь работает с оверлеем;
//   * показываем системный курсор и снимаем ClipCursor
//
// ВАЖНО: окна игр на DirectInput/raw input читают
// устройства напрямую, мимо очереди оконных сообщений. Подклассинг WndProc их
// не остановит
#pragma once

#include <windows.h>

namespace cast::overlay {

    class InputHook {
    public:
        // Подклассит окно игры. Вызывать из потока, владеющего окном
        static bool Install(HWND hwnd);

        // Снимает подклассинг и восстанавливает исходный WndProc
        static void Remove();

        // Активен ли перехват (окно успешно подклассено)
        static bool IsActive();

        // Вызывается при смене видимости оверлея — применяет/снимает курсор.
        // Безопасно вызывать из любого места: если хук не активен — no-op
        static void OnVisibilityChanged(bool visible);
    };

}